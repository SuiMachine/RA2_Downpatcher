		internal bool VerifyOriginFiles(string path)
		{
			bool correct = true;
			var md5 = MD5.Create();

			patcherForm.AppendLog($"Verifying files Red Alert 2 v 1.0006 files!");
			foreach (var keyPair in OriginMD5Hashes)
			{
				var filePath = Path.Combine(path, keyPair.Key);
				if (File.Exists(filePath))
				{
					if(keyPair.Value != null)
					{
						using (var stream = File.OpenRead(filePath))
						{
							byte[] hash = md5.ComputeHash(stream);
							for (int i = 0; i < hash.Length; i++)
							{
								if(keyPair.Value[i] != hash[i])
								{
									correct = false;
									patcherForm.AppendLog($"File: {filePath} failed MD5 check!");
									break;
								}								
							}
						}
					}
				}
				else
				{
					patcherForm.AppendLog($"File: {filePath} doesn't exist");
					correct = false;
				}
			}
			patcherForm.AppendLog($"All files verified correctly!");
			patcherForm.UpdateProgress(10);
			return correct;
		}

		public bool VerifyOriginFilesExist(string path, out List<string> missingFiles)
		{
			var filesFound = Directory.GetFiles(path, "*.*", SearchOption.AllDirectories).Select(x => x.ToLower()).ToArray();
			var filesToFind = OriginMD5Hashes.Select(x => x.Key.ToLower()).ToArray();

			missingFiles = new List<string>();

			foreach (var fileToFind in filesToFind)
			{
				if (!filesFound.Any(x => x.Contains(fileToFind)))
				{
					missingFiles.Add(fileToFind);
				}
			}

			return missingFiles.Count == 0;
		}

